# Creating Function

a = 2
b = 10

# add function adds a and b, returns the result
def add(a,b):
    return(a + b)

# mult function multiplies a and b, returns the result
def mult(a,b):
    return(a * b)

# sub function subtracts a and b, returns the result
def sub(a,b):
    return(a - b)

# div function divides a and b, returns the result
def div(a,b):
    return(a / b)

# main function 
def main():
    print(add(a,b))
    print(mult(a,b))
    print(sub(a,b))
    print(div(a,b))

main()
