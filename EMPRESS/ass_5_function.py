# Creating Function

a = 2
b = 10

# add function adds x and y, returns the result
def add(x,y):
    return(x + y)

# mult function multiplies x and y, returns the result
def mult(x,y):
    return(x * y)

# sub function subtracts x and y, returns the result
def sub(x,y):
    return(x - y)

# div function divides x and y, returns the result
def div(x,y):
    return(x / y)

# main function 
def main():
    print(add(a,b))
    print(mult(a,b))
    print(sub(a,b))
    print(div(a,b))

main()
